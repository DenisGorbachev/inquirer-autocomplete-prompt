function searchCountries(input = '') {
  return new Promise((resolve) => {
    setTimeout(() => {
      const results = countries.filter((s) =>
        s.toLowerCase().includes(input.toLowerCase())
      );

      const all = results.map((r) => ({
        value: r,
        name: r,
        description: `${r} is a great place to visit`,
        disabled: false,
      }));

      // disable the second choice as an example and insert separator
      if (all[1]) {
        all[1].disabled = true;
        all.splice(1, 0, new Separator());
      }

      resolve(all);
    }, 300 + 30);
  });
}

function upperFirst(input) {
  return input.charAt(0).toUpperCase() + input.slice(1);
}

const answer = await autocomplete({
  message: 'Select a country (suggest only)',
  source: searchCountries,
  suggestOnly: true,
  searchText: 'Searching for countries...',
  emptyText: 'No countries found.',
  transformer: (input, { isFinal }) => {
    if (isFinal) {
      return 'You selected: ' + upperFirst(input);
    }

    return upperFirst(input);
  },
  validate(value) {
    if (!value) {
      return 'Please select a country';
    }
    return true;
  },
  default: 'Norway',
  pageSize: 7,
});

